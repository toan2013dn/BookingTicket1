package entity;

/**
 *
 * @author Hi
 */
public class Ticket {
    private int id;
    private int cid;

    public Ticket(int id, int cid) {
        this.id = id;
        this.cid = cid;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }
    
}
